function coordsToSVG(coords) {
    var parser = new DOMParser();
    const polygon = coords.map(e => e.map(f => f * 6400000))

    const minLats = Math.min(...polygon.map(e => e[1]));
    const minLngs = Math.min(...polygon.map(e => e[0]));
    const maxLats = Math.max(...polygon.map(e => e[1]));
    const maxLngs = Math.max(...polygon.map(e => e[0]));
    document.body.appendChild(parser.parseFromString(`<svg version="1.2"
    xmlns="http://www.w3.org/2000/svg"
    viewBox="0 0 ${Math.ceil(maxLngs - minLngs)} ${Math.ceil(maxLats - minLats)}"
    width="50%"
    >
    <style>
        .s5 {fill: brown }
    </style>
    <path  fill-rule="evenodd" class="s5" d="M${polygon.map(e => {
        const lng = (e[0] - minLngs).toFixed(2);
        const lat = (maxLats - e[1]).toFixed(2);
        return `${lng} ${lat} `;
    }).join('L')}z" />
</svg>`, 'image/svg+xml').querySelector('svg'));
}


